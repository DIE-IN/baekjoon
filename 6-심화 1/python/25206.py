l = 0 # 전공과목별 (학점 × 과목평점)의 합
a = 0 # 학점의 총합
t = {"A+": 4.5, "A0": 4.0, "B+": 3.5, "B0": 3.0, "C+": 2.5, "C0": 2.0, "D+": 1.5, "D0": 1.0, "F": 0.0}
for i in range(20):
	n, s, r = input().split(" ") # 과목 학점 등급
	s = float(s)
	if r != "P":
		a += s
		l += s * t[r] # 학점 × 과목평점
print(round(l / a, 6)) # 전공평점